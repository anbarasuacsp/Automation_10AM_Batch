package Java_05052025;

public class ConditionalSwitch {
    public static void main(String[] args) {
        int days=5;
        switch (days){
            case 1:
                System.out.println("To day is a Moday");
                break;
            case 2:
                System.out.println("To day is a Tuesday");
                break;
            case 3:
                System.out.println("To day is a Wednesday");
                break;
            default:
                System.out.println("Given count is not declared");
        }

    }
}
